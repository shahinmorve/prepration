// Function Name: calculateSquareArea
 // Description: This function calculates the area of a square based on 
//  the side length provided as the parameter sideLength.
//  The result is returned using the return statement and
//   is stored in the area variable.

function calculateSquareArea( sideLength){
    return sideLength * sideLength;

}
let area=calculateSquareArea(5);
console.log("area of square",area);