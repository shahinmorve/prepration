// Function Name: None (Anonymous Function)
// Description: This is an example of an anonymous function expression.
//  It assigns a function that multiplies two numbers to the multiply variable. 
// The function is then called with 6 and 7,
//  and the result is stored in the result variable.
let multiply=function (a,b){
return a*b
}
let result =multiply(6,7)
console.log("multiplication of numbers",result)